package it.mobileapp.impl.repository.spring.jpa;

import it.mobileapp.impl.repository.entities.Product;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;

import java.util.Optional;

/**
 * Spring Data Repository
 */
public interface IProductEntityRepository extends JpaRepository<Product,Integer> {

    /**
     * Finding a product by name
     * @param name Product's name
     * @return Product data
     */
    Optional<Product> findByName(String name);

    // if exist almeno una sim con il prodotto attivo
    @Query("select count(c) from Sim c join c.products p where :product member of c.products")
    Long existASimWithThisProduct(@Param("product") Product product);

}
